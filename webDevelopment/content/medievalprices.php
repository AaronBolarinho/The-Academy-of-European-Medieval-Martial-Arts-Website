<blockquote>
  <p>Note: It was mandatory in England for all freemen to own certain types of weapons and
  armor. (In 1181, every freeman having goods worth 10 marks (1 mark = 13s 4d) had to have a
  mail shirt, a helmet, and a spear. All other freemen should have helmet, spear, and
  gambeson (quilted armor) [4], p. 39.) Later, the government stored arms and armour in
  churches for use; in the 13th century anyone with an income of L2-L5 (wealthy peasants)
  had to have bows; archery practice became compulsory on Sundays and holidays. You may know
  that the extreme range of the longbow was 400 yards, but did you know that a statute of
  Henry VIII no one over 24 could practice at a range of less than 220 yards? (See [4], p.
  95 and elsewhere). </p>
</blockquote>

