<blockquote>
  <p>Note: mail is chain mail; almost all the rest is plate-armor. The armor of the knight
  in 1374 was probably mail with some plates; same for Gloucester's. Mail was extremely
  susceptible to rust, and was cleaned by rolling it in sand and vinegar in a barrel.
  Pauldrons are shoulder plates; morions are open helms, burgonets and bascinets closed
  helms; and a target refers to any of a number different kind of shields. Armor of proof is
  tested during the making with blows or shots from the strongest weapons of the time; if a
  weapon is listed, the armor does not claim to be proof against everything, only that it is
  proof up to that weapon's strength (eg pistol proof is not musket proof, but may be sword
  proof). All plate armor was lined with cloth, to pad the wearer, quiet the armor, and
  reduce wear between the pieces. This, along with the necessary straps, was a significant
  amount of the expense. An armorer asking for money to set up shop in 1624 estimated
  production costs and profit for a number of different types of armor: I give two examples
  below ([5], pp. 189-190). </p>
</blockquote>

